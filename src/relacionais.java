public class relacionais {
    public static void main(String[] args) {
        String nome1 = "JAVA";
        String nome2 = "JAVA";

        System.out.println("nome1 == nome2 " + (nome1 == nome2));
        System.out.println("nome1 != nome2 " + (nome1 != nome2));

        int numero1 = 1;
        int numero2 = 2;

        System.out.println("numero1 > numero2 " + (numero1 > numero2));
        System.out.println("numero1 >= numero2 " + (numero1 >= numero2));
        System.out.println("numero1 < numero2 " + (numero1 < numero2));
        System.out.println("numero1 <= numero2 " + (numero1 <= numero2));


    }
}
